# print 'Tuhin' 10 times
i = 1
while i <= 10:
    print('Tuhin', i)
    i += 1

# print 1-10
i = 1

while i <= 10:
    print(i)
    i += 1

# print 10-1
i = 10

while i >= 1:
    print(i)
    i -= 1

# print ('Tuhin' with CSE 5 times) total 5 time
i = 1

while i <= 5:
    print('Tuhin', end=" ")

    j = 5
    while j >= 1:
        print('CSE', end=" ")
        j -= 1

    i += 1
    print()
